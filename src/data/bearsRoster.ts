/**
 * Get player by ESPN ID
 */
export function getPlayerById(espnId: string): RosterPlayer | undefined {
  return BEARS_ROSTER[espnId];
}

/**
 * Get player by name (first + last)
 */
export function getPlayerByName(firstName: string, lastName: string): RosterPlayer | undefined {
  return Object.values(BEARS_ROSTER).find(
    (p) => p.firstName.toLowerCase() === firstName.toLowerCase() && 
           p.lastName.toLowerCase() === lastName.toLowerCase()
  );
}

